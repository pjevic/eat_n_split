import { useState } from "react";
import Button from "./Button";

export default function FormSplitBill({ selectedFriend, onSplitBill, key }) {
  const [bill, setBill] = useState("");
  const [paidByUser, setPaidByUser] = useState("");
  const [whoIsPaying, setWhoIsPaying] = useState("user");

  const paidByFriend = bill ? bill - paidByUser : "";

  function handleSubmit(e) {
    e.preventDefault();

    if (!bill || !paidByUser) return;

    onSplitBill(whoIsPaying === "user" ? paidByFriend : -paidByUser);
  }

  return (
    <form key={key} onSubmit={handleSubmit} className="form-split-bill">
      <h2>Split the bill with {selectedFriend.name}</h2>

      <label>ğŸ’° Bill value</label>
      <input
        type="text"
        value={bill}
        onChange={(e) => setBill(Number(e.target.value))}
      />

      <label>ğŸ§ğŸ»â€â™‚ï¸ Your expense</label>
      <input
        type="text"
        value={paidByUser}
        onChange={(e) =>
          setPaidByUser(
            Number(e.target.value) > bill ? paidByUser : Number(e.target.value)
          )
        }
      />

      <label>ğŸ‘«ğŸ» {selectedFriend.name}'s expense</label>
      <input type="text" disabled value={paidByFriend} />

      <label>ğŸ¤‘ Who is paying the bill</label>
      <select value={whoIsPaying} onChange={(e) => setWhoIsPaying(e.target.value)}>
        <option value="user">You</option>
        <option value="frined">{selectedFriend.name}</option>
      </select>

      <Button>Split bill</Button>
    </form>
  );
}
